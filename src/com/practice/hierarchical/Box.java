package com.practice.hierarchical;

public class Box {
    float height;
    float width;
    float length;

    Box()
    {
        this.height=1;
        this.length=1;
        this.width=1;

    }
    Box(float height,float width,float length)
    {
        this.height=height;
        this.width=width;
        this.length=length;
    }
}
